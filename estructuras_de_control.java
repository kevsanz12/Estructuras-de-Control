public class estructuras_de_control {
    public static void main(String[] args) {
        int numeroIf = -2;

        if (numeroIf >= 1)
        {
            System.out.println("positivo");
        }
        else if(numeroIf <= -1)
        {
            System.out.println("negativo");
        }
        else
        {
            System.out.println("igual a cero");
        }
    }
}

